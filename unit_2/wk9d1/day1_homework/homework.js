// What is Z-index?
// Z-index is a CSS property that defines the order of overlapping HTML elements. Elements with a higher index will be placed on top of elements with a lower index.

// What is a Vendor Prefix and What is it's Purpose?
// A vendor prefix is used to add browser specific styles or properties that are not supported by all browsers. It allows developers to use new features of CSS without worrying about compatibility issues with older versions of web.

